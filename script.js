const loreList = document.getElementById("lore-list");
const searchInput = document.getElementById("search");
const modal = document.getElementById("modal");
const modalTitle = document.getElementById("modal-title");
const modalImage = document.getElementById("modal-image");
const modalDescription = document.getElementById("modal-description");
const closeModal = document.querySelector(".close");

// Funktion zum Anzeigen der Lore-Titel
function displayLore(data) {
    loreList.innerHTML = "";
    data.forEach(item => {
        const div = document.createElement("div");
        div.classList.add("lore-item");
        div.textContent = item.title;
        div.addEventListener("click", () => openModal(item));
        loreList.appendChild(div);
    });
}

// Öffnet das Modal mit den Lore-Infos
function openModal(item) {
    modalTitle.innerHTML = item.title;
    modalImage.src = item.image;
    modalDescription.innerHTML = item.description;
    modal.style.display = "block";

    setTimeout(adjustModalPosition, 50); // Korrekte Positionierung nach Laden
}

// Schließt das Modal
function closeModalHandler() {
    modal.style.display = "none";
}

// Passt die Position des Modals an
function adjustModalPosition() {
    let modalContent = document.querySelector(".modal-content");
    if (!modalContent) return;

    let windowHeight = window.innerHeight;
    let modalHeight = modalContent.offsetHeight;
    let rect = modalContent.getBoundingClientRect();

    if (modalHeight > windowHeight || rect.bottom > windowHeight) {
        modalContent.style.top = "10vh";
        modalContent.style.transform = "translate(-50%, 0)";
        modalContent.style.maxHeight = "80vh";
    } else {
        modalContent.style.top = "50%";
        modalContent.style.transform = "translate(-50%, -50%)";
    }
}

// Filterfunktion für die Suche
function filterLore() {
    const searchTerm = searchInput.value.toLowerCase();
    const filteredLore = loreData.filter(item =>
        item.title.toLowerCase().includes(searchTerm) ||
        item.description.toLowerCase().includes(searchTerm)
    );
    displayLore(filteredLore);
}

// Event-Listener für Suchfunktion
searchInput.addEventListener("input", filterLore);

// Event-Listener für Modalschließung
closeModal.addEventListener("click", closeModalHandler);
window.addEventListener("click", (event) => {
    if (event.target === modal) closeModalHandler();
});

// Modal-Größe beim Fenster-Resize anpassen
window.addEventListener("resize", adjustModalPosition);

// Anzeige aller Einträge beim Start
displayLore(loreData);